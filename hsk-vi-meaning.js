  function looksEnglish(value) {
    const text = clean(value).toLowerCase();
    if (!text) return false;
    if (PLAIN_VI_MEANINGS.has(text)) return false;
    if (/packing list|kangxi|measure word|source:|^\(?mw\b|\b(to|the|and|or|of|for|with|from|make|get|go|do|be|is|are|indicates|particle|radical)\b/.test(text)) return true;
    return !hasVietnameseMarks(text) && /[a-z]/i.test(text);
  }

  function protect(store, value) {
    const key = "§" + store.length + "§";
    store.push(value);
    return key;
  }

  function restore(store, value) {
    return value.replace(/§(\d+)§/g, (_, index) => store[Number(index)] || "");
  }

  function escapeRegExp(value) {
    return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
  }

  function translatePart(part) {
    const original = clean(part)
      .replace(/\((?:[^)]*Kangxi radical[^)]*|Kangxi radical \d+)\)/gi, "")
      .replace(/\bKangxi radical \d+\b/gi, "")
      .replace(/\bsth\.?\b/gi, "something")
      .replace(/\bsb\.?\b/gi, "somebody")
      .replace(/\bmw\b/gi, "measure word");
    if (!original) return "";

    const exact = EXACT[original.toLowerCase()];
    if (exact) return exact;

    const store = [];
    let text = original.toLowerCase();
    const sortedPhrases = PHRASES.slice().sort((a, b) => b[0].length - a[0].length);
    for (const [english, vietnamese] of sortedPhrases) {
      const re = new RegExp("(^|[^a-z])" + escapeRegExp(english.toLowerCase()) + "(?=$|[^a-z])", "g");
      text = text.replace(re, (match, prefix) => prefix + protect(store, vietnamese));
    }

    text = text
      .replace(/\(([^)]*)\)/g, (_, inner) => {
        const translated = translatePart(inner);
        return translated ? "(" + protect(store, translated) + ")" : "";
      })
      .replace(/[a-z][a-z'-]*/gi, (token) => {
        const lower = token.toLowerCase();
        if (STOP.has(lower)) return "";
        if (WORDS[lower]) return protect(store, WORDS[lower]);
        return "";
      })
      .replace(/[0-9]+/g, (number) => number)
      .replace(/[?]/g, "")
      .replace(/[,:]/g, ";")
      .replace(/[()]/g, "")
      .replace(/\s*;\s*/g, "; ")
      .replace(/(?:;\s*){2,}/g, "; ")
      .replace(/\s+/g, " ")
      .replace(/^[;,\s]+|[;,\s]+$/g, "");

    let translated = clean(restore(store, text));
    if (translated.includes(FALLBACK_TEXT) && translated !== FALLBACK_TEXT) {
      translated = clean(translated.replaceAll(FALLBACK_TEXT, "").replace(/\s*;\s*/g, "; "));
    }
    return translated || FALLBACK_TEXT;
  }

  function translateMeaning(value) {
    const raw = clean(value);
    if (!raw) return "";
    if (!looksEnglish(raw)) return raw;
    const exact = EXACT[raw.toLowerCase()];
    if (exact) return finalizeMeaning(exact);
    const override = window.HSK_VI_OVERRIDES?.[raw];
    if (override) return finalizeMeaning(override);
    const parts = raw
      .split(/\s*(?:;|\|)\s*/)
      .map(translatePart)
      .filter(Boolean);
    const translatedParts = parts.filter((part) => part !== FALLBACK_TEXT);
    const unique = Array.from(new Set(translatedParts.length ? translatedParts : parts));
    return finalizeMeaning(unique.join("; ") || FALLBACK_TEXT);
  }

  function finalizeMeaning(value) {
    return clean(value)
      .replace(/\s*\((?:Kangxi|Khang Hy|Khang Hi|khang hy|khang hi|căn|bộ thủ)[^)0-9]*\d+[^)]*\)/gi, "")
      .replace(/\s*\((?:Kangxi|Khang Hy|Khang Hi|khang hy|khang hi|căn|bộ thủ)[^)]*\)/gi, "")
      .replace(/\s*\|\s*/g, "; ")
      .replace(/\s*;\s*/g, "; ")
      .replace(/(?:;\s*){2,}/g, "; ")
      .replace(/\s+\./g, ".")
      .replace(/\s+/g, " ")
      .replace(/^[;,\s]+|[;,\s]+$/g, "");
  }

