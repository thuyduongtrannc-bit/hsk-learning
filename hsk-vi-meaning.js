  function hasTag(word, tag) {
    return Array.isArray(word.tags) && word.tags.includes(tag);
  }

  function sourceText(word) {
    return clean(word.sourceMeaning || "").toLowerCase();
  }

  function pinyinText(word) {
    return clean(word.pinyin).replace(/\s*,\s*/g, " / ");
  }

  function sentenceMeaning(word) {
    return clean(word.meaning || word.fullMeaning || "từ này").replace(/\s*;\s*/g, ", ");
  }

  function capitalize(value) {
    const text = clean(value);
    return text ? text.charAt(0).toUpperCase() + text.slice(1) : text;
  }

  function uniqueClean(parts) {
    const seen = new Set();
    const result = [];
    for (const part of parts.map(clean).filter(Boolean)) {
      const key = part.toLowerCase();
      if (!seen.has(key)) {
        seen.add(key);
        result.push(part);
      }
    }
    return result;
  }

  function typeNote(word) {
    const source = sourceText(word);
    if (/particle|indicates/.test(source)) return "trợ từ";
    if (/measure word|\bmw\b/.test(source)) return "lượng từ";
    if (word.type && word.type !== "từ vựng") return word.type;
    return "";
  }

  function isNotePart(part) {
    const text = clean(part).toLowerCase();
    return /biểu thị|trợ từ|lượng từ|từ tượng thanh|bộ thủ|khang hy|viết tắt|tỉnh|nguyên tắc|âm thanh/.test(text);
  }

  function displayMeaning(word, translatedMeaning) {
    const special = SPECIAL_DISPLAYS[word.hanzi];
    if (special) return special;

    const parts = uniqueClean(clean(translatedMeaning).split(/\s*;\s*/));
    if (!parts.length) return { meaning: clean(translatedMeaning), note: "" };

    const main = [];
    const notes = [];
    const maxMainParts = parts.length > 3 ? 1 : 2;
    for (const part of parts) {
      if (isNotePart(part) || main.length >= maxMainParts) {
        notes.push(part);
      } else {
        main.push(part);
      }
    }
    if (!main.length && notes.length) main.push(notes.shift());

    const parentheticalNotes = [];
    const mainText = finalizeMeaning(main.join("; "))
      .replace(/\s*\(([^)]*)\)/g, (_, inner) => {
        parentheticalNotes.push(inner);
        return "";
      })
      .trim();
    const noteParts = uniqueClean([typeNote(word), ...parentheticalNotes, ...notes]);
    return {
      meaning: mainText || finalizeMeaning(translatedMeaning),
      note: finalizeMeaning(noteParts.join("; "))
    };
  }

